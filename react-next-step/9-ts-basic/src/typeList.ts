/* TypeScriptの基本の型*/

//boolean
const bool: boolean = true;

// number
const number: number = 0;

// string
const str: string = "HOGE";

// Array
const arr1: Array<number> = [1, 2, 3];
const arr2: number[] = [0, 1, 2];

// tuple
let tuple: [number, string] = [0, "HOGE"];

// any(使わないようにする)
let any1: any = false;

// null
let null1: null = null;

//undefined
let undefined1: undefined = undefined;

//object
let obj1: object = {};
let obj2: { id: number; name: string } = { id: 0, name: "A" };
